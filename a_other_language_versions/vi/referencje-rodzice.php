            <div class="col-2" id="rodzice">
              <p>Tôi xin giới thiệu các lớp học trong Konwersatorium Muzyczne, con gái Helena của tôi đang học ở đây.</p>
              <p>Cháu nhập học Konwersatorium Muzyczne  vừa vô tình và cũng vừa theo yêu cầu. Định cho học thử một tháng, nhưng sau tháng đó thì cô con gái 6 tuổi, mà trước đây chưa hề học chơi bất cứ một nhạc cụ nào, đã quen biết rất nhiều nốt nhạc và ngày càng thích chơi đàn piano. Sau kỳ nghỉ hè thì mọi chuyện đã rõ ràng là vẫn sẽ tiếp tục học ở trường, một tháng sau đó thì trong nhà chúng tôi xuất hiện thêm chiếc đàn piano. Thời gian trôi qua, Helena vẫn rất vui mừng cắp sách tới trường. Tôi cho là đó là công lao của cô giáo tuyệt vời Bogna Kołodziej, nhờ cô giáo mà Helena rất thích thú ngồi học đàn, không hề bị ép buộc, mà còn rất quyết tâm học.</p>
              <p>Khi tôi hỏi Helena tại sao con thích đi học môn này thế, thì nghe thấy cháu trả lời là cô Bogna thật dễ thương và cháu có thể học thuộc những nốt nhạc mới, quen dần với các nhạc cụ và khi cháu đi học thì rất vui, trong lớp có nhiều trò chơi thú vị, không chỉ ngồi chơi đàn piano.</p>
              <p>Tôi cũng đánh giá tốt trường này bởi vì Helena có thể phát triển về âm nhạc với lĩnh vực sâu rộng. Cháu không chỉ được học chơi đàn piano, mà được đón nhận kiến thức âm nhạc thực sự. Tôi rất tự hào khi nghe thấy trong đài có bản nhạc thì Helena có thể làm cho tôi ngạc nhiên: ”Mẹ ơi, đây là bài của nhạc sỹ Vivaldi đấy”.</p>
              <p>Tôi chân thành khuyên nên chọn Trường Nhạc, nếu các bạn muốn con cái mình phát triển và có sự nhạy cảm âm nhạc, vì học ở đây không hề bị bệnh stress như các cháu bé vẫn thường có khi học ở các trường nhạc truyền thống. Ở đây trẻ em có cơ hội học thêm và lại cảm thấy rất vui khi chơi đàn.</p>
              <p class="podpis">– Marta Mazurkiewicz</p>
              <p>Chúng tôi cùng với con trai rất hân hạnh được làm quen với cả 2 cô giáo khi đến thi thử. Cả tôi và cả Kuba ra khỏi trường như là đang mê ngủ. Rất chuyên nghiệp, mà rất đầy nhiệt huyết dành cho âm nhạc, cho trẻ em, cho sự ngiệp giáo dục, ngoài ra nơi này là sự huyền bí. Có điều rõ ràng là trẻ em đến đây học là vì các cháu thích học, chứ không phải vì bắt buộc. Chúng tôi chưa cho cháu nhập học chỉ là do hoàn cảnh kinh tế chưa cho phép, nhưng chúng tôi hoàn toàn ủng hộ và đồng ý với những ý kiến của mẹ cháu Helena – mẹ của Kuba.</p>
              <p class="podpis">– Magdalena Czerwińska-Strzelczyk</p>
              <p>Tôi là mẹ của hai cháu gái, cả hai đang cùng học ở chỗ cô Bogna. Chỉ sau vài buổi học là có thể thấy là các cháu rất thích đi học Konwersatorium Muzyczne và không chỉ một lần đã thấy là rất khó mà xếp lịch cho công việc tự luyện tập ở nhà cho cả 2 cháu. Vì chỉ có 1 chiếc đàn piano, chúng tôi mua ngay sau khi các cháu mới học được có 4 buổi và như vậy trong nhà chúng tôi tiếng đàn đã xuất hiện thường xuyên và cố định.</p>
              <p>Thấy các cháu ham học, tôi cũng kiên trì học chơi đàn cùng với cô Grazyna.</p>
              <p>Khi được học cùng với cô Bogna và với bà Grazyna thì chúng tôi không chỉ học các nốt nhạc. Đó còn là sự vui chơi, thời gian tiêu khiển, rất dễ chịu, được nghỉ ngơi thư giãn và nói chung thời gian trôi rất thú vị. Học chơi đàn piano (nhỏ) và thỉnh thoảng đàn dương cầm (to), tất cả 3 chúng tôi có cảm nhận âm nhạc hoàn toàn khác nhau, nghe nhạc một cách khác nhau và đối với mỗi chúng tôi thì âm nhạc đều có giá trị phi thường.</p>
              <p>Làm cha mẹ, chúng tôi cảm thấy rất tự hào là âm nhạc đã cho phép các con của chúng tôi rộng mở tâm hồn và các cháu đang có rất nhiều điều thú vị và sự hài lòng.</p>
              <p>Tài năng sư phạm phi thường của cô Bogna thật ấn tượng, niềm đam mê công việc của cô thường được biệu lộ ở trong từng tiết học, với những phương pháp dạy đầy sự sáng chế và lại rất thú vị. Đó là một thí dụ về người giáo viên làm việc với lòng đam mê và có phương pháp giảng dạy làm cho các học trò của mình bị “lây” niềm say mê âm nhạc. Với tư các là người lớn, mà mới bắt đầu học chơi đàn piano cùng với bà Grazyna, tôi không còn nghi ngờ gì về tài năng của cô Bogna chẳng qua là “được sinh ra”.</p>
              <p>Chúng tôi chân thành cảm ơn và xin giới thiệu với các bạn nơi này, đó là một chốn mê hoặc, là một điểm đến tuyệt vời, ấm cúng, cùng với các cô giáo chuyên nghiệp.</p>
              <p class="podpis">– Ewelina Stobiecka (người mẹ tự hào)</p>
              <p>Tôi là cha của cháu Natalia 5 tuổi rưỡi.</p>
              <p>Vào tháng 05.2014 Natalia bắt đầu cuộc phiêu lưu với âm nhạc của mình, học chơi đàn piano ở Trường Nhạc trên phố Poznańska 39 ở Vác-sa-va do 2 cô giáo Grażyna và Bogna Kolodziej dạy. Từ lúc mới học, cô Bogna dạy cháu.</p>
              <p>Tôi rất ngạc nhiên về phương pháp giảng dạy mà các cô áp dụng ở trường. Các cô giáo áp dụng cả những viên Lego, những mảnh ghép puzzli, những trò chơi khác nhau trong các buổi học nốt nhạc, học nút đàn, nhạc cụ và lịch sử các nhà soạn nhạc, điều đó làm cho con gái tôi không muốn rời buổi học để đi về nhà.</p>
              <p>Không cần phải đợi lâu, mà cũng đã xuất hiện những hiệu quả tích cực, bởi vì cháu đã nhận ra được các âm điệu của những bài nhạc và các tác giả, ngoài ra cháu còn tự chơi được những bài thánh ca đầu tiên hay là bài hát “Trăm tuổi” chúc sinh nhật, mang lại nhiều niềm vui và sự ấm cúng cho ngôi nhà của chúng tôi.Có thể viết rất nhiều về những niềm vui khi học đàn.</p>
              <p>Cô Bogna mang lại sự vui tươi trong từng giờ học.</p>
              <p>Ít ra tôi cũng có thể nghe được điều đó qua cánh cửa.</p>
              <p class="podpis">– Grzegorz Sienkiewicz</p>
              <p>Con gái của chúng tôi bắt đầu học đàn piano trong trường khi cháu mới được 5 tuổi. Trước đó thì chúng tôi không biết cháu có thích chơi đàn hay không, hoặc là chúng tôi sẽ có thể giúp gì cho cháu. Sau nửa năm học đàn, không chỉ con gái chúng tôi thích thú ngồi chơi đàn, mà chồng tôi cũng bắt đầu đánh đàn.</p>
              <p>Cùng với chồng, chúng tôi vui mừng là thành quả của con gái là do sự cộng tác của cô giáo, của cha mẹ và của chính đứa trẻ. Nhưng rõ ràng là công lao của cô giáo là chính, bởi vì cô tạo ra hướng phát triển và nội dung học tập. Không có người dẫn đường tốt thì khó mà thành công. Chúng tôi rất cảm ơn cô Bogna về sự nhiệt tình và niềm tin vào khả năng của cháu.</p>
              <p class="podpis">– Katarzyna Proctor</p>
              <p>Tôi là mẹ của chái trai 4 tuổi, đả học năm thứ hai (hai lần trong tuần, mỗi lân phút), do có Bogna Kołodziej dạy.</p>
              <p>Suốt quá trình này, cô Bogna cho thấy được đó là một người rất ham mê công việc, các buổi học đầy tính sáng tạo và cô biết chọn ra những chương trình học cho từng cháu một cách rất tốt, tùy theo nhu cầu của từng đứa trẻ. Cô Bogna đã có mối quan hệ tốt với con trai tôi, cháu có thêm được kiến thức và kỹ năng âm nhạc (không một lần con trai đã làm chúng tôi phải ngạc nhiên về sự thông thạo đối với các loại nhạc cụ ở các buổi hòa nhạc ở Nhạc viện và thông thạo cả các tác phẩm nữa).</p>
              <p>Các buổi học rất thú vị và đó là sự phát triển âm nhạc toàn năng. Con trai tôi rất thích thú học, cháu là một đứa trẻ đầy năng lượng và không ngồi yên được lâu tại một chổ.</p>
              <p>Ngoài ra, cô Bogna rất dễ thương, dễ chịu, kiên trì và rất yêu thích trẻ em. Tôi chân thành khuyên nên học cùng cô Bogna.</p>
              <p class="podpis">– D.W.</p>
              <p>Tôi làm việc với tư cách là nhà dựng cảnh và trang trí nội thất trong các hiện trường quay phim. Hàng ngày trong công việc, tôi phải lo toan với vấn đề rất thiếu thời gian, vì nhiều tình huống thay đổi khác nhau và nói chung rất hay bị lo lắng. Hồi thơ ấu tôi cũng có học chơi đàn piano. Sau nhiều năm tôi nhớ lại loại nhạc cụ này. Vậy là trong nhà tôi đã xuất hiện chiếc đàn piano. Tôi đã nghĩ là công việc tôi khó khăn như vậy thì khó mà kết hợp được với công việc học đàn ở Trường Nhạc.</p>
              <p>Vậy mà cũng đã thu xếp được và như vậy tôi cảm thấy rất hài lòng. Giờ học là khoảng thời gian chỉ dành cho riêng tôi. Khi đó tôi nghỉ ngơi, không lo nghĩ gì chuyện khác và tôi đang làm một công việc hoàn toàn khác. Một dạng kiểu như như là được điều trị bằng âm nhạc. Học xong tiết học, tôi cảm thấy sảng khoái, đầy nhiệt huyết và nhiều sáng kiến mới cho cuộc sống. Và tất nhiên là tôi chơi đàn ngày càng khá hơn. Gần đây tôi xếp lịch ghi tên cả chồng tôi vào lớp học đàn. Tôi có niềm mơ ước là đến một khi nào đó chúng tôi sẽ có thể cùng chơi với nhau trên 1 chiếc đàn – biểu diễn 4 tay.</p>
              <p class="podpis">– Ineza Bodyk</p>
